/**
 * Markdown export — "CMO Brief".
 *
 * Aggregates everything we know about the customer into a one-page
 * markdown doc they can forward internally. The structure is the brief
 * a fractional CMO would write: TL;DR up top, then the diagnostic
 * sections (mention rate, hallucinations, audit gaps, portal coverage,
 * refresh queue), then the recommended actions.
 *
 * Sales weapon: prospects who say "let me share this with the team"
 * walk away with a doc, not a screenshot. CS weapon: we can read it
 * to know what's actually wrong with a customer's site without
 * clicking through 9 dashboard tabs.
 */

interface LatestScores { chatgpt?: number; gemini?: number; overall?: number; readiness?: number; mentions?: number }
interface QueryRow {
  query?: string;
  chatgpt?: { mentioned?: boolean };
  gemini?: { mentioned?: boolean };
  hallucinations?: Array<{ claim?: string; truth?: string; severity?: string }>;
}

function sanitize(s: string | null | undefined): string {
  if (!s) return "—";
  return String(s).replace(/\n/g, " ").trim();
}

function fmtPct(n: number | undefined): string {
  if (typeof n !== "number" || Number.isNaN(n)) return "—";
  return `${Math.round(n)}%`;
}

function fmtDate(iso?: string | null): string {
  if (!iso) return "—";
  try {
    return new Date(iso).toLocaleDateString("en-IN", { year: "numeric", month: "short", day: "numeric" });
  } catch {
    return iso;
  }
}

function downloadResponse(filename: string, body: string): NextResponse {
  return new NextResponse(body, {
    status: 200,
    headers: {
      "Content-Type": "text/markdown; charset=utf-8",
      "Content-Disposition": `attachment; filename="${filename}"`,
      "Cache-Control": "no-store",
    },
  });
}

async function userOwnsCompany(userId: string, companyId: string): Promise<boolean> {
  const db = getServiceClient();
  const { data } = await db
    .from("companies")
    .select("id")
    .eq("id", companyId)
    .eq("owner_id", userId)
    .maybeSingle();
  return !!data;
}

